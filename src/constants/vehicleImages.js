// Default fallback image
export const DEFAULT_VEHICLE_IMAGE = '/placeholder.svg';

/**
 * Get vehicle images by brand and model
 */
export function getVehicleImages(brand, model) {
  if (!brand || !model) {
    return [];
  }

  const brandKey = brand.toLowerCase().replace(/\s+/g, '').replace(/-/g, '');
  const modelKey = model.toLowerCase().replace(/\s+/g, '').replace(/-/g, '');

  // Try exact match
  if (VEHICLE_IMAGES[brandKey]?.[modelKey]?.images) {
    return VEHICLE_IMAGES[brandKey][modelKey].images.filter(img => img !== '');
  }

  // Fallback to brand default
  if (VEHICLE_IMAGES[brandKey]?.default?.images) {
    return VEHICLE_IMAGES[brandKey].default.images.filter(img => img !== '');
  }

  // Final fallback
  return [];
}

/**
 * Get a single vehicle image (first available) by brand and model
 */
export function getVehicleImage(brand, model) {
  if (!brand || !model) {
    return DEFAULT_VEHICLE_IMAGE;
  }

  const brandKey = brand.toLowerCase().replace(/\s+/g, '').replace(/-/g, '');
  const modelKey = model.toLowerCase().replace(/\s+/g, '').replace(/-/g, '');

  // Try exact match - use thumbnail first, then first image
  if (VEHICLE_IMAGES[brandKey]?.[modelKey]) {
    const vehicleData = VEHICLE_IMAGES[brandKey][modelKey];
    if (vehicleData.thumbnail && vehicleData.thumbnail !== '') {
      return vehicleData.thumbnail;
    }
    const firstImage = vehicleData.images?.find(img => img !== '');
    if (firstImage) {
      return firstImage;
    }
  }

  // Fallback to brand default
  if (VEHICLE_IMAGES[brandKey]?.default) {
    const defaultData = VEHICLE_IMAGES[brandKey].default;
    if (defaultData.thumbnail && defaultData.thumbnail !== '') {
      return defaultData.thumbnail;
    }
    const firstImage = defaultData.images?.find(img => img !== '');
    if (firstImage) {
      return firstImage;
    }
  }

  // Final fallback
  return DEFAULT_VEHICLE_IMAGE;
}
